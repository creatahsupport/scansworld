        .box { width: 100%; max-width: 450px; padding: 30px; border: 1px solid #ddd; border-radius: 12px; box-shadow: 0 8px 16px rgba(0,0,0,0.1); background: #fff; }
        .form-group { margin-bottom: 20px; }
        .form-control { width: 100%; padding: 12px; border: 1px solid #ccc; border-radius: 6px; text-align: center; font-size: 1.5rem; letter-spacing: 5px; }
        .btn-primary { width: 100%; padding: 12px; background-color: #007bff; border: none; color: white; border-radius: 6px; cursor: pointer; font-weight: 600; }
        .error { color: #dc3545; margin-bottom: 15px; text-align: center; background: #f8d7da; padding: 10px; border-radius: 4px; }
        .qr-section { text-align: center; margin-bottom: 25px; border-bottom: 1px solid #eee; padding-bottom: 20px; }
        .qr-section img { max-width: 180px; margin-bottom: 15px; }
        .setup-steps { font-size: 0.9rem; color: #555; text-align: left; margin-bottom: 15px; }
        .setup-steps ol { padding-left: 20px; }
    </style>
</head>
<body>
<div class="container">
    <div class="box">
        <h2 style="text-align: center; margin-bottom: 20px;">2FA Verification</h2>
        
        <?php if (!$user['two_factor_enabled']): ?>
            <div class="qr-section">
                <h5>Setup Two-Factor Authentication</h5>
                <p>Scan this QR code with Google Authenticator or Authy to secure your account.</p>
                <img src="<?php echo $qr_code_url; ?>" alt="QR Code">
                <p><strong>Secret Key:</strong> <code style="background: #f4f4f4; padding: 2px 5px;"><?php echo $user['two_factor_secret']; ?></code></p>
            </div>
            <div class="setup-steps">
                <ol>
                    <li>Open your authenticator app.</li>
                    <li>Scan the QR code above or enter the secret key.</li>
                    <li>Enter the 6-digit code generated by the app below.</li>
                </ol>
            </div>
        <?php else: ?>
            <p style="text-align: center;">Enter the 6-digit code from your authenticator app to continue.</p>
        <?php endif; ?>

        <?php if ($error): ?>
            <div class="error"><?php echo $error; ?></div>
        <?php endif; ?>

        <form method="POST">
            <div class="form-group">
                <input type="text" name="code" class="form-control" maxlength="6" pattern="\d{6}" placeholder="000000" required autofocus autocomplete="off">
            </div>
            <button type="submit" class="btn-primary">Verify & Login</button>
            <div style="text-align: center; margin-top: 20px;">
                <a href="index.php" style="color: #666; text-decoration: none;">&larr; Back to Login</a>
            </div>
        </form>
    </div>
</div>
