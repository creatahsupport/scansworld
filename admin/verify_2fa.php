<?php
include("../includes/config.php");
include("../includes/TOTP.php");
// session_start(); // Already called in config.php

if (!isset($_SESSION['temp_user_id'])) {
    header("Location: index.php");
    exit();
}

$user_id = $_SESSION['temp_user_id'];

// Fetch user status and secret
$stmt = $con->prepare("SELECT id, user_name, role, two_factor_secret, two_factor_enabled FROM admin WHERE id = ?");
$stmt->bind_param("i", $user_id);
$stmt->execute();
$result = $stmt->get_result();
$user = $result->fetch_assoc();

// If no secret exists, generate one now
if (empty($user['two_factor_secret'])) {
    $new_secret = TOTP::generateSecret();
    $up_stmt = $con->prepare("UPDATE admin SET two_factor_secret = ? WHERE id = ?");
    $up_stmt->bind_param("si", $new_secret, $user_id);
    $up_stmt->execute();
    $user['two_factor_secret'] = $new_secret;
}

$error = '';
if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $code = isset($_POST['code']) ? trim($_POST['code']) : '';

    if (TOTP::verifyCode($user['two_factor_secret'], $code)) {
        // Success
        
        // If it was the first time setup, mark as enabled
        if (!$user['two_factor_enabled']) {
            $enable_stmt = $con->prepare("UPDATE admin SET two_factor_enabled = 1 WHERE id = ?");
            $enable_stmt->bind_param("i", $user_id);
            $enable_stmt->execute();
        }

        $_SESSION['user_name'] = $user['user_name'];
        $_SESSION['admin_id'] = $user_id;
        $_SESSION['role'] = $user['role'];
        $_SESSION['last_activity'] = time();
        
        unset($_SESSION['temp_user_id']);
        unset($_SESSION['temp_user_name']);
        unset($_SESSION['temp_role']);
        
        session_regenerate_id(true);
        header("Location: dashboard.php");
        exit();
    } else {
        $error = "Invalid 2FA code. Please try again.";
    }
}

$qr_code_url = TOTP::getQRCodeGoogleUrl($user['user_name'], $user['two_factor_secret'], 'ScansWorld');
?>
