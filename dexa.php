                <li> No fasting required - Walk in anytime. Avoid calcium supplements 24 hours before only.</li>
              </ul>
              <h3 class="h5 mt-1">T-score Guide:</h3>
              <ul>
                <li>1.0 or above = Normal bone density</li>
                <li>1.0 to –2.5 = Osteopenia (low bone density - early warning)</li>
                <li>2.5 or below = Osteoporosis (significantly low density - high fracture risk)</li>
              </ul>
              <h2 class="fw-semibold">Who should get a DEXA Scan?</h2>
              <ul>
                <li>Women above 45 - especially post-menopausal women</li>
                <li>Men above 60</li>
                <li>Long-term steroid users (3+ months)</li>
                <li>People with a history of fractures</li>
                <li>Family history of osteoporosis</li>
                <li>Low body weight</li>
                <li>Smokers and heavy drinkers</li>
                <li>Monitoring bone health treatment effectiveness</li>
              </ul>
              <h2 class="fw-semibold">What conditions does a DEXA scan detect?</h2>
              <ul>
                <li>Osteoporosis – extreme loss of bone density with high fracture risk</li>
                <li>Osteopenia – early bone thinning before osteoporosis develops</li>
                <li>Risk of vertebral fractures, particularly in the lower back</li>
                <li>Hip fracture risk assessment – for older adults</li>
                <li>Secondary osteoporosis – from steroid use, thyroid problems or malabsorption</li>
                <li>Monitoring treatment response – tracking improvement after bone health medication</li>
                <li>Body composition analysis - ratio of fat mass to lean mass </li>
              </ul>
              <h2 class="fw-semibold">What to expect during your DEXA scan?</h2>

