              <!-- What is a DEXA Scan? -->
            <h3 class="fw-semibold mt-3">What is a DEXA Scan?</h3>
            <p>DEXA stands for Dual-Energy X-ray Absorptiometry. It uses two low-energy X-ray beams to measure the density of minerals, primarily calcium in your bones. The denser your bones, the stronger they are. The result is expressed as a T-score, which tells you how your bone density compares to that of a healthy young adult.</p>
            <p>The scan focuses primarily on the hip and lumbar spine, the two areas most prone to fractures in people with osteoporosis. It is painless, takes only 15–20 minutes and is considered the most accurate and reliable bone density test available today.</p>
            

