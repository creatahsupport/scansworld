                <li>Modern flexible endoscopes – High definition camera systems for visualisation of the GI tract in
                  clear detail.</li>
                <li>Same day report – Documented and communicated procedure findings the same day.</li>
                <li>Minimally Invasive – There is no need for major surgery. Most patients go home the same day.</li>
                <li>3 centres in Chennai – Available in <a href="https://scansworldonchamiersroad.com/aminjikarai"
                    target="_blank">Aminjikarai, </a>Nandanam and Nanganallur.</li>
                <li>Clear instructions for preparation – Thorough pre-procedure instructions so you arrive fully
                  prepared.</li>
              </ul>
              <h2 class="fw-semibold">What conditions do these procedures diagnose?</h2>
              <h3 class="h5 mt-1">Endoscopy Detects:</h3>
              <ul>
                <li>Stomach ulcers and gastritis</li>
                <li>Acid reflux / GERD (chronic heartburn)</li>
                <li>Oesophageal inflammation or stricture</li>
                <li>Upper GI bleeding source identification</li>
                <li>Barrett's oesophagus</li>
                <li>Hiatus hernia</li>
                <li>Swallowing difficulties (dysphagia)</li>
                <li>H. pylori infection</li>

              </ul>
              <h3 class="h5 mt-1">Colonoscopy Detects:</h3>
              <ul>
                <li>Colon polyps - and removes them.</li>
                <li>Colorectal cancer - early detection.</li>
                <li>Inflammatory bowel disease (IBD).</li>
                <li>Crohn's disease and ulcerative colitis.</li>
                <li>Lower GI bleeding - source identification.</li>
                <li>Diverticulosis / diverticulitis.</li>
                <li>Chronic diarrhoea or constipation.</li>
                <li>Unexplained anaemia.</li>
              </ul>
              <h2 class="fw-semibold">When should you get an Endoscopy or Colonoscopy?</h2>
              <ul>
                <li>Persistent stomach pain, bloating, or nausea.</li>
                <li>Chronic heartburn or acidity not responding to medicines.</li>
                <li>Difficulty swallowing or sensation of food sticking.</li>
                <li>Blood in the faeces or black, tarry stools containing blood.</li>
                <li>Weight loss that can’t be explained.</li>
                <li>Change in bowel habits for a long time, constipation or diarrhea.</li>
                <li>Colon cancer or polyps in the family history.</li>
                <li>Routine screening for colorectal cancer in adults 45 years of age and older.</li>
              </ul>
              <h2 class="fw-semibold">What to expect before during & after</h2>
              <h3 class="fw-semibold">Before The Procedure</h3>
              <p>For an endoscopy, you will need to fast for at least 6–8 hours beforehand. For a colonoscopy, bowel
                preparation is required the day before our team will give you specific instructions and a preparation
                kit at the time of booking. Please inform us of any medications you are currently taking.</p>
              <h3 class="h5 mt-1">During The Procedure</h3>
              <p>A mild sedative is typically given to help you relax. The procedure itself is performed by an
                experienced gastroenterologist.You may feel mild pressure or bloating, but significant pain is uncommon.
                Our team will monitor you throughout the procedure and ensure your comfort.</p>
              <h3 class="h5 mt-1">After The Procedure</h3>
              <p>Most patients rest for 30–60 minutes at our facility before being discharged. You may feel mild
                bloating or a sore throat (for endoscopy), which resolves within a few hours. A responsible adult
                should
                accompany you if sedation has been given. Most patients return to normal activities the same day or
                the
                next morning.</p>
              <p><b>Note :</b> If a polyp is found during colonoscopy, it can often be removed during the same
                procedure saving you a second visit.</p>
