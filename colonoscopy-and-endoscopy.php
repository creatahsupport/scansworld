              <h2 class="fw-semibold">Endoscopy vs Colonoscopy - What's the difference?</h2>
              <h3 class="h5 mt-1">Endoscopy (Upper g.i.)</h3>
              <p>A thin, flexible tube is passed through the mouth to look at the oesophagus (food pipe), stomach and
                the upper part of the small intestine. To diagnose and sometimes treat problems with the upper part of
                the digestive system.
                Procedure time 15-30 minutes.
              </p>
              <h3 class="h5 mt-1">Colonoscopy (Lower gi)</h3>
              <p>A long, flexible tube is gently passed through the rectum to examine the whole large bowel (colon).
                Used to detect polyps, cancers, inflammation and bleeding in the lower digestive tract.</p>
              <h2 class="fw-semibold">Why Choose Scans World for Colonoscopy & Endoscopy in Chennai?</h2>
              <ul>
                <li>Trained gastroenterologists – Procedures performed by trained GI specialists, not by general
